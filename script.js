function show1() {
    let pr = document.getElementsByClassName('p2')[0];
    let img3=document.getElementById('img1');
    if (pr.style.display === 'none' || pr.style.display === '') {
        pr.style.display = 'block';
        img3.src='./assets/images/icon-minus.svg';
      } else {
        pr.style.display = 'none';
        img3.src='./assets/images/icon-plus.svg';
      }
  }
